    private void calculateNonTeach(){
        Float overtimeHr, absentHr, payRate, overtimePay, overtimeRate, allowance, deduction, payRoll;
        try{
            overtimeHr = Float.valueOf(nonOver.getText());
            absentHr = Float.valueOf(nonAbsent.getText());
            payRate = Float.valueOf(nonRate.getText());
            if(overtimeHr < 0 || absentHr < 0 || payRate < 0){
                JOptionPane.showMessageDialog(new JFrame(), "Input parameters cannot be negative!");
                return;
            }
        }catch(NumberFormatException e){
            return;
        }

        if(overtimeHr > 48 || overtimeHr < 0){
            System.out.println("Overtime value should be between 0 and 48.");
            return;
        }
        else{
            overtimeRate = (payRate / 30 / 8) * 1.20f;
            overtimePay = overtimeHr * overtimeRate;
        }
        
        if(nonAllowance.getText().matches("[0-9]+")){
            Float allowanceVal = Float.valueOf(nonAllowance.getText());
            allowance = payRate * (allowanceVal / 100);
            System.out.println("This thing is valid for allowance");
        }
        else{
            allowance = 0.00f;
        }
        
        deduction = absentHr * (payRate / 30 / 8);
        payRoll = (payRate + overtimePay + allowance) - deduction;

        overloadDisp.setText(String.valueOf(overtimePay));
        deductionsDisp.setText(String.valueOf(deduction));
        allowanceDisp.setText(String.valueOf(allowance));
        payrollDisp.setText(String.valueOf(payRoll));
    }
