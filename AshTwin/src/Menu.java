    private void calculateTeach(){
        float teachHour = Float.parseFloat(teachingHours.getText());
        float rate = Float.parseFloat(payRate.getText());
        float absentHr = Float.parseFloat(absentHours.getText());
        if(teachHour < 0 || rate < 0 || absentHr < 0){
            JOptionPane.showMessageDialog(new JFrame(), "Negative input parameters are invalid!");
            return;
        }
        //Overload is zero by default so as to allow simple overwriting.
        float overloadHr = 0;
        float deductionVal = rate * absentHr;
        deductions.setText(String.valueOf(deductionVal));
        float basicPay = (teachHour * rate) - deductionVal;
        
        //Overwrites overload value (0 by default) if monthly hour is higher than max normal hour (96) treshold to calculate overload hours 
        if(teachHour > 96.00 && teachHour <= 128){
            overloadHr = teachHour - 96.00f;
            //Only calculates the rate of the given hour before the treshold limit.
            basicPay = (96.00f * rate) - deductionVal;

        }
        //Program can't accept any monthly hours higher than the maximum overload hour limit.
        else if(teachHour > 128){
            JOptionPane.showMessageDialog(new JFrame(), "Teaching hours musn't exceed overload hours limit!");
            System.out.println("TEACH HOUR CAN'T GO OVER OVERTIME LIMIT");
            return;
        }
        //Adds to deduction if the given monthly hour is lower than minimum monthly hour (48)
        else if (teachHour <= 48 && teachHour>= 0){
            deductionVal = deductionVal - ((48 - teachHour) * rate);
        }
        overloadHours.setText(String.valueOf(overloadHr));
        float overPay = overloadHr * (rate * 1.20f);
        overloadPay.setText(String.valueOf(overPay));
        float allowanceVal = basicPay * 0.10f;
        allowance.setText(String.valueOf(allowanceVal));
        float monthPay = (teachHour * rate + overPay + allowanceVal) - deductionVal;
        System.out.println("testing");
        totalPay.setText(String.valueOf(monthPay));
        System.out.println("The monthly pay of this teaching employee is: " + monthPay);
        

    }
